    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // TODO add your handling code here:

        int id = Integer.parseInt(textCodigoIdMateria.getText());
        
        
        
        Materia encontrada = new Materia();
        encontrada = materiaData.buscarMateriaPorId(id);
        
    if (!Objects.isNull(encontrada)) {
                // Rellenado de campos luego de buscar
                
                textNombreMateria.setText(encontrada.getNombre());
                textAnioMateria.setText(Integer.toString(encontrada.getAnioMateria()));
                checkBoxActiva.setSelected(encontrada.isActivo());
                botonBorrar.setEnabled(true);
                botonActualizar.setEnabled(true);
                botonGuardar.setEnabled(false);
                
                
            } else {
                
                checkBoxActiva.setSelected(false);

    }
    }//GEN-LAST:event_botonBuscarActionPerformed

